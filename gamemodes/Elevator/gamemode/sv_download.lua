local ValidExtensions = {"vmt", "mdl", "wav","mp3"}

local function AddPrecacheResources()

	for i=1, #ResourceList do
		local File = ResourceList[i]

		local ext = string.GetExtensionFromFilename(File)
				
		if table.HasValue(ValidExtensions, ext) then

			local DownloadFile = string.lower( string.sub( File, 1 ) )				

			//resource.AddFile( DownloadFile )
			
			if ( ext == "mp3" || ext == "wav" ) then
				util.PrecacheSound( DownloadFile )
			end
			
			if ( ext == "mdl" ) then
				util.PrecacheModel( DownloadFile )
			end

		end

	end

end

